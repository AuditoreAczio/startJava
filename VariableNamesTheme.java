public class VariableNamesTheme {
    public static void main(String[] args) {

        System.out.println("\n1. Разные переменные: ");

        // цифра
        int number = 7;
        System.out.println(number);

        // сумма чисел
        int sum = 1;
        System.out.println(sum);

        // произведение чисел
        int productOfNumbers = 6;
        System.out.println(productOfNumbers);

        // максимально число
        int max = 43;
        System.out.println(max);

        // количество десятков
        byte numberOfTens = 6;
        System.out.println(numberOfTens);

        // вес собаки
        int dogWeight = 14;
        System.out.println(dogWeight);

        // целое число
        int originalNumber = 5;
        System.out.println(originalNumber);

        // процент по вкладу
        float interestRate = 15;
        System.out.println(interestRate);

        // код ошибки
        int errorCode = 144;
        System.out.println(errorCode);

        // счет в игре
        int gameScore = 23;
        System.out.println(gameScore);

        // максимальная длина
        int maxLenght = 34;
        System.out.println(maxLenght);

        // выбор
        int choose = 3;
        System.out.println(choose);

        // тип сообщений
        int messageType = 4;
        System.out.println(messageType);

        // математическое выражение
        float mathExpression = 67;
        System.out.println(mathExpression);

        // случайное число
        int randomNumber = 5;
        System.out.println(randomNumber);

        // число нулей
        int numberOfZeros = 6;
        System.out.println(numberOfZeros);

        // стоимость кофе на вынос
        int costCoffeeToGo = 43;
        System.out.println(costCoffeeToGo);

        // дата начала
        int startDate = 23;
        System.out.println(startDate); 

        // имя работника месяца
        String nameEmployeeMonth = "Alexsanr";
        String space = " ";
        String lastName = "Prohorov";
        System.out.println(nameEmployeeMonth + space + lastName);

        // заголовок электронной книги
        int ebookTitle = 3;
        System.out.println(ebookTitle);

        // окончание диапазона
        int endOfRange = 6;
        System.out.println(endOfRange);

        // размер
        int size = 5;
        System.out.println(size);

        // вместимость
        int capacity = 7;
        System.out.println(capacity);

        // счетчик
        int counter = 8;
        System.out.println(counter);

        // путь до файла
        int pathToFile = 9;
        System.out.println(pathToFile);

        // количество чисел в каждой строке
        int numberOfNumbersInEachRow = 4;
        System.out.println(numberOfNumbersInEachRow);

        // переменная хранит символ &
        char ampersand = '&';
        System.out.println(ampersand);

        System.out.println("\n2. boolean переменные");

        // сотни равны?
        boolean hasEqualHundreds = true;
        System.out.println(hasEqualHundreds);

        // компьютер включен?
        boolean isTurnedOnPc = false;
        System.out.println(isTurnedOnPc);

        // есть равные цифры
        boolean hasEualNumbers = true;
        System.out.println(hasEualNumbers);

        // создано
        boolean create = false;
        System.out.println(create);

        // пустое 
        boolean isEmpty = false;
        System.out.println(isEmpty);

        // активное 
        boolean isActivity = true;
        System.out.println(isActivity);

        // новое
        boolean isNew = false;
        System.out.println(isNew);

        // электронная почта действительная
        boolean isEmailValid = true;
        System.out.println(isEmailValid);

        // имеются уникальные строки
        boolean hasUniqueStrings = true;
        System.out.println(hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");

        // старый universally unique identifier
        long oldUuid = 78;
        System.out.println(oldUuid);

        // Производитель оперативной памяти
        int ram = 0;
        System.out.println(ram);

        // емкость жесткого диска
        int hddc = 67;
        System.out.println(hddс);

        // протокол передачи гипертекста
        int http = 895;
        System.out.println(http);

        // сокращенный uniform resource locator
        int url = 90;
        System.out.println(url);

        // новый идентификатор клиента
        int newClientId = 12376;
        System.out.println(newClientId);

        // кодировка american standard code for information interchange
        int ascii = 87;
        System.out.println(ascii);
    }
}